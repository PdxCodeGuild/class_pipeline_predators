# Part 1 
number = input('Enter a grade')

number = int(number)

print(number)


if number >= 90:
    print("Your grade is an A")

elif number >= 80:
    print("Your grade is a B")

elif number >= 70:
    print("Your grade is a C")

elif number >= 60:
    print("Your grade is a D")

else:
    print("Your grade is an F")

